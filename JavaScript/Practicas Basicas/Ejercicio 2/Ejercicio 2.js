var variable1 = 100;
var variable2 = 50;

/*
Operadores Aritmeticos
Aumento progresivo = ++ || Modulo = % || Division = / || Multiplicacion = * || Suma = + || Resta = -
*/

console.log("Numero 1", variable1);
console.log("Numero 2", variable2);

var resultado_suma = variable1 + variable2;
console.log(resultado_suma);

var resultado_resta = variable1 - variable2;
console.log(resultado_resta);

var resultado_division = variable1 / variable2;
console.log(resultado_division);

var resultado_multiplicacion = variable1 * variable2;
console.log(resultado_multiplicacion);

var resultado_modulo = variable1 % variable2;
console.log(resultado_modulo);